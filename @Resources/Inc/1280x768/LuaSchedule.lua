		SKIN:Bang('!SetOption', 'IndexDay2Name', 'Text', D2Name)
		SKIN:Bang('!SetOption', 'IndexDay3Name', 'Text', D3Name)
		local D1_1=tostring(SKIN:GetVariable('Week1Day1_1'))
		local D1_2=tostring(SKIN:GetVariable('Week1Day1_2'))
		local D1_3=tostring(SKIN:GetVariable('Week1Day1_3'))
		local D1_4=tostring(SKIN:GetVariable('Week1Day1_4'))
		local D1_5=tostring(SKIN:GetVariable('Week1Day1_5'))
		local D1_6=tostring(SKIN:GetVariable('Week1Day1_6'))
		SKIN:Bang('!SetOption', 'IndexDay1Text', 'Text', D1_1..NS..D1_2..NS..D1_3..NS..D1_4..NS..D1_5..NS..D1_6)
		local D2_1=tostring(SKIN:GetVariable('Week1Day2_1'))
		local D2_2=tostring(SKIN:GetVariable('Week1Day2_2'))
		local D2_3=tostring(SKIN:GetVariable('Week1Day2_3'))
		local D2_4=tostring(SKIN:GetVariable('Week1Day2_4'))
		local D2_5=tostring(SKIN:GetVariable('Week1Day2_5'))
		local D2_6=tostring(SKIN:GetVariable('Week1Day2_6'))
		SKIN:Bang('!SetOption', 'IndexDay2Text', 'Text', D2_1..NS..D2_2..NS..D2_3..NS..D2_4..NS..D2_5..NS..D2_6)
