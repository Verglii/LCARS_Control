		local D3_2=tostring(SKIN:GetVariable('Week1Day3_2'))
		local D3_3=tostring(SKIN:GetVariable('Week1Day3_3'))
		local D3_4=tostring(SKIN:GetVariable('Week1Day3_4'))
		local D3_5=tostring(SKIN:GetVariable('Week1Day3_5'))
		local D3_6=tostring(SKIN:GetVariable('Week1Day3_6'))
		SKIN:Bang('!SetOption', 'IndexDay3Text', 'Text', D3_1..NS..D3_2..NS..D3_3..NS..D3_4..NS..D3_5..NS..D3_6)
	else
		SKIN:Bang('!SetOption', 'IndexDay1Name', 'Text', D4Name)
		SKIN:Bang('!SetOption', 'IndexDay2Name', 'Text', D5Name)
		SKIN:Bang('!SetOption', 'IndexDay3Name', 'Text', D6Name)
		local D1_1=tostring(SKIN:GetVariable('Week1Day4_1'))
		local D1_2=tostring(SKIN:GetVariable('Week1Day4_2'))
		local D1_3=tostring(SKIN:GetVariable('Week1Day4_3'))
		local D1_4=tostring(SKIN:GetVariable('Week1Day4_4'))
		local D1_5=tostring(SKIN:GetVariable('Week1Day4_5'))
		local D1_6=tostring(SKIN:GetVariable('Week1Day4_6'))
		SKIN:Bang('!SetOption', 'IndexDay1Text', 'Text', D1_1..NS..D1_2..NS..D1_3..NS..D1_4..NS..D1_5..NS..D1_6)
		local D2_1=tostring(SKIN:GetVariable('Week1Day5_1'))
		local D2_2=tostring(SKIN:GetVariable('Week1Day5_2'))
		local D2_3=tostring(SKIN:GetVariable('Week1Day5_3'))
		local D2_4=tostring(SKIN:GetVariable('Week1Day5_4'))
		local D2_5=tostring(SKIN:GetVariable('Week1Day5_5'))
		local D2_6=tostring(SKIN:GetVariable('Week1Day5_6'))
		SKIN:Bang('!SetOption', 'IndexDay2Text', 'Text', D2_1..NS..D2_2..NS..D2_3..NS..D2_4..NS..D2_5..NS..D2_6)
