		local D3_2=tostring(SKIN:GetVariable('Week1Day6_2'))
		local D3_3=tostring(SKIN:GetVariable('Week1Day6_3'))
		local D3_4=tostring(SKIN:GetVariable('Week1Day6_4'))
		local D3_5=tostring(SKIN:GetVariable('Week1Day6_5'))
		local D3_6=tostring(SKIN:GetVariable('Week1Day6_6'))
		SKIN:Bang('!SetOption', 'IndexDay3Text', 'Text', D3_1..NS..D3_2..NS..D3_3..NS..D3_4..NS..D3_5..NS..D3_6)
	end
end